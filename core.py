letter_values = {"A":1, "J":1, "S":1,
        "B":2, "K":2, "T":2,
        "C":3, "L":3, "U":3,
        "D":4, "M":4, "V":4,
        "E":5, "N":5, "W":5,
        "F":6, "O":6, "X":6,
        "G":7, "P":7, "Y":7,
        "H":8, "Q":8, "Z":8,
        "I":9, "R":9}
VOWELS_LATIN = "AEIOU"
VOWELS_RUSSIAN = "АЕЁИОУЫЭЮЯ"

def is_vowel(char):
    """Проверяет, является ли буква гласной (латиница или кириллица)"""
    return char.upper() in (VOWELS_LATIN + VOWELS_RUSSIAN)

# НОВАЯ ФУНКЦИЯ: получает числовое значение буквы (латиница/кириллица)
def get_letter_value(letter):
    # Латинские буквы
    if 'A' <= letter <= 'Z' or 'a' <= letter <= 'z':
        return letter_values.get(letter.upper(), 0)
    # Русские буквы (включая Ё)
    elif ('А' <= letter <= 'Я' or 'а' <= letter <= 'я' or letter in ('Ё', 'ё')):
        if letter in ('Ё', 'ё'):
            pos = 7  # Ё — седьмая буква
        elif 'А' <= letter <= 'Я':
            pos = ord(letter) - ord('А') + 1
        else:
            pos = ord(letter) - ord('а') + 1
        val = pos % 9
        if val == 0:
            val = 9
        return val
    else:
        return 0  # для прочих символов

def calculate_soul_number(text):
    tn = 0
    if type(text) is int:
        tn += sum(list(map(lambda x: int(x),str(text))))
    if type(text) is str:
        text = "".join(i for i in text if i.isalpha()).upper()
        for i in text:
            if is_vowel(i):
                tn += get_letter_value(i)
    if tn > 9:
        return calculate_soul_number(tn)
    elif tn == 0:
        return 0  # или другое значение по умолчанию
    else:
        return tn



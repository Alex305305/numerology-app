def validate_date(date_str):
    pat = r"^(?:0[1-9]|[12][0-9]|3[01])([./-])(?:0[1-9]|1[0-2])\1(?:19|20)[0-9]{2}$"
    return bool(re.fullmatch(pat, date_str))



import re
import time
# валидация даты
def validate_date(date_str):
    pat = r"^(?:0[1-9]|[12][0-9]|3[01])([./-])(?:0[1-9]|1[0-2])\1(?:19|20)[0-9]{2}$"
    return bool(re.fullmatch(pat, date_str))


# число жизненного пути
def calculate_life_path(date_str):
    pat = r"[0-9]"
    s = re.findall(pat, date_str)
    s = sum(list(map(lambda x: int(x),s)))
    if s > 9:
        return calculate_life_path(str(s))
    else:
        return s


#число души (гласные)
letter_values = {"A":1, "J":1, "S":1,
        "B":2, "K":2, "T":2,
        "C":3, "L":3, "U":3,
        "D":4, "M":4, "V":4,
        "E":5, "N":5, "W":5,
        "F":6, "O":6, "X":6,
        "G":7, "P":7, "Y":7,
        "H":8, "Q":8, "Z":8,
        "I":9, "R":9}
VOWELS = "AEIOU"
def calculate_soul_number(text):
    tn = 0
    if type(text) is int:
        tn += sum(list(map(lambda x: int(x),str(text))))
    if type(text) is str:
        text = "".join(i for i in text if i.isalpha()).upper()
        for i in text:
            if i in VOWELS:
                tn += letter_values[i]
    if tn > 9:
        return calculate_soul_number(tn)
    else:
        return tn


#число ума (согласные)
def calculate_mind_number(text):
    tn = 0
    if type(text) is int:
        tn += sum(list(map(lambda x: int(x),str(text))))
    if type(text) is str:
        text = "".join(i for i in text if i.isalpha()).upper()
        for i in text:
            if i not in VOWELS:
                tn += letter_values[i]
    if tn > 9:
        return calculate_mind_number(tn)
    else:
        return tn



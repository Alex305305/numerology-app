def animate_stars(duration=2):
    """
    Анимация мерцающих звёзд на заданное время (в секундах)
    """
    end_time = time.time() + duration
    stars = ["✨", "🌟", "💫", "⭐", "🌠"]

    print("\n🔮 Предсказание в процессе...", end="", flush=True)

    while time.time() < end_time:
        for star in stars:
            print(f"\r🔮 Предсказание в процессе... {star}", end="", flush=True)
            time.sleep(0.2)

    print("\r🔮 Предсказание завершено! ✨✨✨")
    time.sleep(0.5)


